    private void btnExclusaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExclusaoActionPerformed
        try {
            if(txtCodigo.getText().equals("")){
                throw new StringVaziaException("O campo de exclusão não pode ser vazio");
            }
            
            int codigo = Integer.parseInt(txtCodigo.getText());
            
            try {
                jogoController.jogoExcluir(codigo);
                JOptionPane.showMessageDialog(null, "Excluido com sucesso");
            } catch(SQLException ex) {
                JOptionPane.showMessageDialog(null, "Não foi possível excluir o jogo");
                System.out.println("Erro ao excluir jogo" + ex.toString());
            }
            
            for (int i = 0; i < tbl.getRowCount(); i++){
                if(Integer.parseInt(tbl.getValueAt(i, 0).toString()) == codigo) {
                    tbl.removeRow(i);
                    tblJogos.setModel(tbl);
                    break;
                }
            }
        } catch (StringVaziaException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (DadoNaoEncontrado e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnExclusaoActionPerformed

